import { useStore } from "vuex";

import { ElMessage } from 'element-plus'
export default {
  name: 'newLoginComponent',
  emits: ['submit'],
  setup(_, { emit }) {
    const store = useStore();
    const switchCtn = ref(null)
    const Mycard = ref(null)
    const switchC1 = ref(null)
    const switchC2 = ref(null)
    const switchC3 = ref(null)
    const switchCircle = ref([])
    const switchBtn = ref([])
    const aContainer = ref(null)
    const bContainer = ref(null)
    const allButtons = ref([])
    const userLoginForm = reactive({
      username: '',
      password: '',
    })
    const userRegisterForm = reactive({
      email: "",
      username: "",
      password: "",
      confirmPassword: ""
    })



    const changeForm = () => {
      switchCtn.value.classList.add("is-gx");
      switchCtn.value.classList.add("disable");
