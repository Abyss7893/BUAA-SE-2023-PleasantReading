    async function userList() {
      try {
        axios
          .post(
            "http://154.8.183.51/user/login",
            {
              id: userLoginForm.username,
              pwd: userLoginForm.password,
            },
            {
              headers: {
                "Content-Type": "application/json", // 设置请求头为 JSON 格式
              },
            }
          )
          .then((response) => {
            const token = response.data.access;
            // 将令牌存储在本地存储中
            localStorage.setItem("token", token);
            localStorage.setItem("tokenStartTime", new Date().getTime());
            // 更新默认请求头中的令牌
            axios.defaults.headers.common["Authorization"] = `Bearer ${token}`;
            // 登录成功后的操作，例如跳转到其他页面
            const userId = response.data.userId;
            store.commit("setUser", userId);

            ElMessage({
              message: '登陆成功',
              grouping: true,
              type: 'success',
            })
            ending()

            // 登录成功后要获取用户信息储存到vuex中
            axios
              .get(`http://154.8.183.51/user/getinfo/${userLoginForm.username}`)
              .then((response) => {
                // 从响应数据中获取用户信息，并保存到 vuex 的 userInfo 中
                store.commit("updateUserInfo", response.data);

              })
              .catch(() => {
                // 处理错误情况
              });
            axios
              .get(
                `http://154.8.183.51/user/getavatar/${userLoginForm.username}`,
                {}
              )
              .then((response) => {
                const url = response.data.url;
                store.commit("setAvatarUrl", url);

              })
              .catch(() => {
                ElMessage({
                  message: '账号或密码错误',
                  grouping: true,
                  type: 'error',
                })
              });

          })
          .catch(() => {
            // 处理登录错误
            userLoginForm.password = userLoginForm.username = "";
            ElMessage({
              message: '账号或密码错误',
              grouping: true,
              type: 'error',
            })
          });
      } catch (_) {
        // 处理错误
        userLoginForm.password = userLoginForm.username = "";
      }
    }
    async function register() {

      const emailReg = /^([a-zA-Z]|[0-9])(\w|-)+@[a-zA-Z0-9]+\.[a-zA-Z]{2,}$/;
      if (!emailReg.test(userRegisterForm.email)) {
        ElMessage({
          message: '请输入正确的邮箱地址',
          grouping: true,
          type: 'error',
        })

        return;
      }
      if (userRegisterForm.password !== userRegisterForm.confirmPassword) {
        ElMessage({
          message: '两次输入的密码不一致',
          grouping: true,
          type: 'error',
        })

        return
      }
      if (userRegisterForm.username == '') {
        ElMessage({
          message: '账号不能为空',
          grouping: true,
          type: 'error',
        })

        return
      }
      if (userRegisterForm.password == '') {
        ElMessage({
          message: '密码不能为空',
          grouping: true,
          type: 'error',
        })

        return
      }
      try {
        const res = await axios.post('http://154.8.183.51/user/register', userRegisterForm,
          {
            headers: {
              'content-type': 'application/json'
            }
          })
        if (res.status === 200) {
          console.log(res.data.message)
          if (res.data.message === 'success') {
            //注册成功
            ElMessage({
              message: '注册成功',
              grouping: true,
              type: 'success',
            })
            switchBtn.value[0].click();
          }

          else
            ElMessage({
              message: '账号已存在',
              grouping: true,
              type: 'error',
            })
        } else {
          throw new Error('请求失败')
        }

      } catch (error) {
        ElMessage({
          message: '注册失败',
          grouping: true,
          type: 'error',
        })
      }
      userRegisterForm.email = "";
      userRegisterForm.username = "";
      userRegisterForm.password = "";
      userRegisterForm.confirmPassword = "";
    }
    onMounted(() => {

      switchCtn.value = document.querySelector("#switch-cnt")
      switchC1.value = document.querySelector("#switch-c1")
      switchC2.value = document.querySelector("#switch-c2")
      switchC3.value = document.querySelector("#switch-c3")
      Mycard.value = document.querySelector("#mycard")
      aContainer.value = document.querySelector("#a-container")
      bContainer.value = document.querySelector("#b-container")
      mainF()
    })

    return {
